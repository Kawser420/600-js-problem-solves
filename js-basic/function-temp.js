// simple exchange
let a = 10;
let b = 20;
console.log(a, b);
a = b;
b = a;
console.log(a, b);

// temp exchange
let x = 11;
let y = 65;
console.log(x, y);
const temp = x;
x = y;
y = temp;
console.log(x, y);

// another temp exchange example
